function StackS() {

  var stackArray = null     ;
  var top        = 0        ;

  this.init      = init     ;
  this.push      = push     ;
  this.pop       = pop      ;
  this.get_stack = get_stack;
  this.isEmpty   = isEmpty  ;
  this.makeEmpty = makeEmpty;

  init();

  /*----------------------------------------------------------------------------------------- StackS ---*/

  function init()     {
    stackArray = new Array();
    top        = -1         ;
  }
  /*------------------------------------------------------------------------------------------- push ---*/

  function push(obj)    {
    stackArray[++top] = obj; //trace(stackArray[top].get_root());
  }

  /*-------------------------------------------------------------------------------------------- pop ---*/

  function pop()        {
    return stackArray[top--];
  }
  /*-------------------------------------------------------------------------------------- get_stack ---*/
 
  function get_stack()  {
    return stackArray;
  }
  /*---------------------------------------------------------------------------------------- isEmpty ---*/

  function isEmpty()    {
    return (top == -1);
  }
  /*-------------------------------------------------------------------------------------- makeEmpty ---*/

  function makeEmpty()  {
    top = -1;
  }
  /*----------------------------------------------------------------------------------------------------*/
}
