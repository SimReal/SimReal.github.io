/*============================================================================================ Stack ===*/
function Stack() {

  var stack      = null     ;
  var top        = 0        ;

  this.init      = init     ;
  this.push      = push     ;
  this.pop       = pop      ;
  this.isEmpty   = isEmpty  ;
  this.removeAll = removeAll;

  init();

  /*------------------------------------------------------------------------------------------- init ---*/

  function init()       {
    stack = new Array();
    top   = -1         ;
  }
  /*------------------------------------------------------------------------------------------- push ---*/

  function push(obj)    {
    stack[++top] = obj;
  }
  /*-------------------------------------------------------------------------------------------- pop ---*/

  function pop()        {
    return stack[top--];
  }
  /*---------------------------------------------------------------------------------------- isEmpty ---*/

  function isEmpty()    {
    return (top == -1);
  }
  /*-------------------------------------------------------------------------------------- removeAll ---*/

  function removeAll()  {
    top = -1;
  }
  /*----------------------------------------------------------------------------------------------------*/
}
/*======================================================================================================*/